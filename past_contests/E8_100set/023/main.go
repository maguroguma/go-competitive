var (
	n, m int
	P    []int
)

func main() {
	n, m = ReadInt2()
	tmp := ReadIntSlice(n)
	P = []int{0}
	P = append(P, tmp...)

	A := []int{}
	for i := 0; i < len(P); i++ {
		for j := 0; j < len(P); j++ {
			A = append(A, P[i]+P[j])
		}
	}

	sort.Sort(sort.IntSlice(A))
	ans := 0
	for _, a := range A {
		ok := BinarySearch(-1, len(A), func(mid int) bool {
			return a+A[mid] <= m
		})

		if ok >= 0 {
			ChMax(&ans, a+A[ok])
		}
	}
	fmt.Println(ans)
}

// ChMax accepts a pointer of integer and a target value.
// If target value is LARGER than the first argument,
//	then the first argument will be updated by the second argument.
func ChMax(updatedValue *int, target int) bool {
	if *updatedValue < target {
		*updatedValue = target
		return true
	}
	return false
}

func BinarySearch(initOK, initNG int, isOK func(mid int) bool) (ok int) {
	ng := initNG
	ok = initOK
	for int(math.Abs(float64(ok-ng))) > 1 {
		mid := (ok + ng) / 2
		if isOK(mid) {
			ok = mid
		} else {
			ng = mid
		}
	}

	return ok
}

