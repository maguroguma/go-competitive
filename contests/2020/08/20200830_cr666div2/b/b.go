var (
	n int
	A []int64
)

func main() {
	n = readi()
	A = readlls(n)

	sort.Slice(A, func(i, j int) bool {
		return A[i] < A[j]
	})
	// PrintfDebug("%v\n", A)

	base := C1()

	if n > 60 {
		fmt.Println(base)
		return
	}

	ans := base
	for c := int64(2); c <= 100000; c++ {
		tmp := calc(c)
		ChMin(&ans, tmp)
	}
	fmt.Println(ans)
}

func calc(a int64) int64 {
	res := int64(0)

	cur := int64(1)
	res += AbsInt(A[0] - cur)

	F := Kiriage(INF_BIT60, a)

	for i := 1; i <= n-1; i++ {
		if cur > F {
			return INF_BIT60
		}

		cur *= a
		res += AbsInt(A[i] - cur)
	}

	return res
}

func C1() int64 {
	ans := int64(0)
	for i := 0; i < n; i++ {
		ans += AbsInt(A[i] - 1)
	}
	return ans
}

// Kiriage returns Ceil(a/b)
// a >= 0, b > 0
func Kiriage(a, b int64) int64 {
	return (a + (b - 1)) / b
}

// ChMin accepts a pointer of integer and a target value.
// If target value is SMALLER than the first argument,
//	then the first argument will be updated by the second argument.
func ChMin(updatedValue *int64, target int64) bool {
	if *updatedValue > target {
		*updatedValue = target
		return true
	}
	return false
}

// AbsInt is integer version of math.Abs
func AbsInt(a int64) int64 {
	if a < 0 {
		return -a
	}
	return a
}

