var (
	// n, d, m    int
	n, d       int
	m          int64
	A          []int64
	big, small []int64
)

func main() {
	// n, d, m = ReadInt3()
	n, d = ReadInt2()
	m = ReadInt64()
	A = ReadInt64Slice(n)

	for _, a := range A {
		// if a > int64(m) {
		if a > m {
			big = append(big, a)
		} else {
			small = append(small, a)
		}
	}

	// bigは降順, smallは昇順
	sort.Slice(big, func(i, j int) bool { return big[i] > big[j] })
	sort.Slice(small, func(i, j int) bool { return small[i] < small[j] })

	bs := make([]int64, len(big)+1)
	for i := 0; i < len(big); i++ {
		bs[i+1] = bs[i] + big[i]
	}
	ss := make([]int64, len(small)+1)
	for i := 0; i < len(small); i++ {
		ss[i+1] = ss[i] + small[i]
	}

	var ans int64
	smallAll := Sum(small...)
	if len(big) == 0 {
		ans = smallAll
	} else {
		ans = 0
	}
	for i := 1; i <= len(big); i++ {
		cur := bs[i]
		// mini, maxi := d*(i-1), d*i
		mini, maxi := int(Min(int64(n), int64(d)*int64(i-1))), int(Min(int64(n), int64(d)*int64(i)))
		resBig := len(big) - i

		if resBig > maxi {
			continue
		}
		if mini <= resBig && resBig <= maxi {
			cur += smallAll
			ChMax(&ans, cur)
			continue
		}

		nokori := mini - resBig
		if nokori > len(small) {
			continue
		}
		cur += smallAll - ss[nokori]
		ChMax(&ans, cur)
	}
	fmt.Println(ans)
}

// Min returns the min integer among input set.
// This function needs at least 1 argument (no argument causes panic).
func Min(integers ...int64) int64 {
	m := integers[0]
	for i, integer := range integers {
		if i == 0 {
			continue
		}
		if m > integer {
			m = integer
		}
	}
	return m
}

// ChMax accepts a pointer of integer and a target value.
// If target value is LARGER than the first argument,
//	then the first argument will be updated by the second argument.
func ChMax(updatedValue *int64, target int64) bool {
	if *updatedValue < target {
		*updatedValue = target
		return true
	}
	return false
}

// Sum returns multiple integers sum.
func Sum(integers ...int64) int64 {
	var s int64
	s = 0

	for _, i := range integers {
		s += i
	}

	return s
}

