var (
	t int

	n int
	S []rune
)

func main() {
	defer stdout.Flush()

	t = readi()
	for tc := 0; tc < t; tc++ {
		n = readi()
		S = readrs()

		ans := solve()
		printf("%d\n", ans)
	}
}

func solve() int {
	res := 0

	_, C := RunLengthEncoding(S)
	B := len(C)
	fp := 0 // 1文字消すポインタ
	sp := 0 // prefixを消すポインタ
	for sp < B {
		// まず最初に、fpは必ずspと同じかそれより後方に位置させる
		for fp < sp {
			fp++
		}

		// fpは2文字以上あるブロックまで移動させる
		for fp < B && C[fp] <= 1 {
			fp++
		}

		// 「1文字消す」に相当する操作
		if fp < B {
			// 2文字以上あるものがあればそれを消す
			C[fp]--
		} else {
			// しょうがないのでspの最後の1文字を消す
			sp++
		}

		// 1文字消せたら操作完了
		res++

		// prefixを消すに相当するポインタ移動
		sp++
	}

	return res
}

// RunLengthEncoding returns encoded slice of an input.
func RunLengthEncoding(S []rune) ([]rune, []int) {
	runes := []rune{}
	lengths := []int{}

	l := 0
	for i := 0; i < len(S); i++ {
		// 1文字目の場合保持
		if i == 0 {
			l = 1
			continue
		}

		if S[i-1] == S[i] {
			// 直前の文字と一致していればインクリメント
			l++
		} else {
			// 不一致のタイミングで追加し、長さをリセットする
			runes = append(runes, S[i-1])
			lengths = append(lengths, l)
			l = 1
		}
	}
	runes = append(runes, S[len(S)-1])
	lengths = append(lengths, l)

	return runes, lengths
}

// RunLengthDecoding decodes RLE results.
func RunLengthDecoding(S []rune, L []int) []rune {
	if len(S) != len(L) {
		panic("S, L are not RunLengthEncoding results")
	}

	res := []rune{}

	for i := 0; i < len(S); i++ {
		for j := 0; j < L[i]; j++ {
			res = append(res, S[i])
		}
	}

	return res
}

