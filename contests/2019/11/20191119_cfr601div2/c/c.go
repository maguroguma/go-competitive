var book [100000 + 5][][]int

func main() {
	n = ReadInt()
	for i := 0; i < n-2; i++ {
		T = append(T, ReadIntSlice(3))
	}

	memo := make([]int, n+1)
	for i := 0; i < n-2; i++ {
		for j := 0; j < 3; j++ {
			q := T[i][j]
			memo[q]++
		}
	}

	ones := []int{}
	twos := []int{}
	for i := 1; i < n+1; i++ {
		if memo[i] == 1 {
			ones = append(ones, i)
		} else if memo[i] == 2 {
			twos = append(twos, i)
		}
	}

	for i := 0; i < n-2; i++ {
		a, b, c := T[i][0], T[i][1], T[i][2]
		book[a] = append(book[a], []int{a, b, c})
		book[b] = append(book[b], []int{a, b, c})
		book[c] = append(book[c], []int{a, b, c})
	}

	// 先頭のトリプルを見つける
	targetInt := ones[0]
	firstTriple := book[targetInt][0]
	answers := make([]int, 3)
	for i := 0; i < 3; i++ {
		tmp := firstTriple[i]
		if memo[tmp] == 1 {
			answers[0] = tmp
		} else if memo[tmp] == 2 {
			answers[1] = tmp
		} else {
			answers[2] = tmp
		}
	}

	// answersが完成するまでループ
	for len(answers) < n {
		l := len(answers)
		ex, inc1, inc2 := answers[l-3], answers[l-2], answers[l-1]
		// 最大3つの中から該当するものを見つける
		for _, tri := range book[inc2] {
			if sub(tri, ex, inc1, inc2) {
				// inc1, inc2以外の値をanswersに追加する
				answers = append(answers, subsubsub(tri, inc1, inc2))
				break
			}
		}
	}

	fmt.Println(PrintIntsLine(answers...))
}

// triが目的のものだったらtrue
func sub(tri []int, ex, inc1, inc2 int) bool {
	// exを含んだらfalse
	if subsub(tri, ex) {
		return false
	}

	// inc1を含まないならfalse
	if !subsub(tri, inc1) {
		return false
	}
	// inc2を含まないならfalse
	if !subsub(tri, inc2) {
		return false
	}

	return true
}

// triがtargetを含んだらtrue
func subsub(tri []int, target int) bool {
	for _, v := range tri {
		if v == target {
			return true
		}
	}
	return false
}

// inc1, inc2以外の値を返す
func subsubsub(tri []int, inc1, inc2 int) int {
	for _, v := range tri {
		if v != inc1 && v != inc2 {
			return v
		}
	}
	return -1
}

