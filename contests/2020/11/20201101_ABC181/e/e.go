var (
	n, m int
	H, W []int

	D, revD       []int
	dsum, revdsum []int
)

func main() {
	defer stdout.Flush()

	n, m = readi2()
	H = readis(n)
	W = readis(m)
	W = append(W, -INF_B60, INF_B60)

	sort.Sort(sort.IntSlice(H))
	sort.Sort(sort.IntSlice(W))

	// 1のときは例外処理する
	if n == 1 {
		fmt.Println(sub(H[0]))
		return
	}

	revH := Reverse(H)

	D, revD = make([]int, n/2), make([]int, n/2)
	for i := 1; i < n; i += 2 {
		d := abs(H[i] - H[i-1])
		D[i/2] = d
	}
	for i := 1; i < n; i += 2 {
		d := abs(revH[i] - revH[i-1])
		revD[i/2] = d
	}
	dsum, revdsum = make([]int, n/2+1), make([]int, n/2+1)
	for i := 0; i < n/2; i++ {
		dsum[i+1] = dsum[i] + D[i]
		revdsum[i+1] = revdsum[i] + revD[i]
	}
	// debugf("dsum: %v\n", dsum)
	// debugf("revdsum: %v\n", revdsum)

	ans := INF_B60
	for i := 0; i < n; i++ {
		// i番目を先生と組ませる
		if i%2 == 0 {
			base := dsum[i/2] + revdsum[n/2-i/2]

			t := sub(H[i])
			chmin(&ans, base+t)

			// if i == n-1 {
			// 	debugf("H[i]: %d, base: %d, t: %d\n", H[i], base, t)
			// }
		} else {
			base := dsum[i/2] + revdsum[n/2-1-i/2]
			lit := abs(H[i-1] - H[i+1])
			base += lit

			t := sub(H[i])
			chmin(&ans, base+t)
		}
	}

	fmt.Println(ans)
}

// hの人を先生と組ませる場合の、差の最小値
func sub(h int) int {
	ok := BinarySearch(len(W)-1, 0, func(mid int) bool {
		return h <= W[mid]
	})

	res := INF_B60
	chmin(&res, abs(h-W[ok]))
	if ok-1 >= 0 {
		chmin(&res, abs(h-W[ok-1]))
	}
	if ok+1 < len(W) {
		chmin(&res, abs(h-W[ok+1]))
	}

	return res
}

func BinarySearch(initOK, initNG int, isOK func(mid int) bool) (ok int) {
	ng := initNG
	ok = initOK
	for int(math.Abs(float64(ok-ng))) > 1 {
		mid := (ok + ng) / 2
		if isOK(mid) {
			ok = mid
		} else {
			ng = mid
		}
	}

	return ok
}

func Reverse(A []int) []int {
	res := []int{}

	n := len(A)
	for i := n - 1; i >= 0; i-- {
		res = append(res, A[i])
	}

	return res
}

