var (
	r, c int
	A    [][]int
)

func main() {
	r, c = ReadInt2()
	for i := 0; i < r; i++ {
		row := ReadIntSlice(c)
		A = append(A, row)
	}

	ans := 0
	BruteForceByBits(r, func(mask []bool) {
		B := [][]int{}
		for i := 0; i < r; i++ {
			row := make([]int, len(A[i]))
			copy(row, A[i])
			B = append(B, row)
		}

		for i := 0; i < r; i++ {
			if mask[i] {
				for j := 0; j < c; j++ {
					B[i][j] = (B[i][j] + 1) % 2
				}
			}
		}

		res := 0
		for j := 0; j < c; j++ {
			one := 0
			for i := 0; i < r; i++ {
				one += B[i][j]
			}
			res += Max(one, r-one)
		}

		ChMax(&ans, res)
	})

	fmt.Println(ans)
}

// Max returns the max integer among input set.
// This function needs at least 1 argument (no argument causes panic).
func Max(integers ...int) int {
	m := integers[0]
	for i, integer := range integers {
		if i == 0 {
			continue
		}
		if m < integer {
			m = integer
		}
	}
	return m
}

// ChMax accepts a pointer of integer and a target value.
// If target value is LARGER than the first argument,
//	then the first argument will be updated by the second argument.
func ChMax(updatedValue *int, target int) bool {
	if *updatedValue < target {
		*updatedValue = target
		return true
	}
	return false
}

// BruteForceByBits(10, func(b []bool) { fmt.Println(b) }
// -> [false, false, false, false, false, false, false, false, false, false]
//    [true, false, false, false, false, false, false, false, false, false]
//    [false, true, false, false, false, false, false, false, false, false]
//    [true, true, false, false, false, false, false, false, false, false]
//    [false, false, true, false, false, false, false, false, false, false]
//    ...
// originated from ktateish@atcoder
// e.g.: https://atcoder.jp/contests/abc167/submissions/13042361
func BruteForceByBits(l int, fn func([]bool)) {
	L := 1 << uint(l)
	mask := make([]bool, l)
	for i := 0; i < L; i++ {
		for j := 0; j < l; j++ {
			k := l - j - 1
			if i&(1<<uint(j)) == 0 {
				mask[k] = false
			} else {
				mask[k] = true
			}
		}
		fn(mask)
	}
}

